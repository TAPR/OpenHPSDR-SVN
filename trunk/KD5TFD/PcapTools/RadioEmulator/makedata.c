void cvt24BitsIntTo3Bytes(int num, unsigned char *bytep) {
    *(bytep+2) = num & 0xff;
    num = num >> 8;
    *(bytep+1) = num & 0xff;
    num = num >> 8;
    *bytep = num & 0xff;
    return;
}

void cvt16BitsIntTo3Bytes(short num, unsigned char *bytep) {
    *(bytep+1) = num & 0xff;
    num = num >> 8;
    *bytep = num & 0xff;
    return;
}



