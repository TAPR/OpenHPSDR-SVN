/*
 * These are the ddc register definitions for the Rev AB QS1R */

#ifndef _QS1R_DDC_REGS_H_
#define _QS1R_DDC_REGS_H_

#define MCR 0x300
#define NCR 0x301
#define NSCR 0x302
#define NFRE 0x303
#define NPHA 0x304
#define C2SR 0x305
#define C2DEC 0x306
#define C5SR 0x307
#define C5DEC 0x308
#define RCR 0x309
#define RDEC 0x30A
#define RAO 0x30B
#define RTAP 0x30C
#define RSRV 0x30D

#define DDC_RAM 0x100
#define DDC_COEF 0x000

#endif
